<?php

namespace Entity;

use ClassInterface\ProductInterface;

Class Chair implements ProductInterface {

    private $id = NULL;
    private $name = NULL;
    private $price = NULL;


    public function getId() {
        return $this->id;
    }

    public function getName() {
        return $this->name;
    }

    public function getPrice() {
        return $this->price;
    }

    public function setId($id) {
        $this->id = $id;
        return $this;
    }

    public function setName($name) {
        $this->name = $name;
        return $this;
    }

    public function setPrice($price) {
        $this->price = $price;
        return $this;
    }


}
